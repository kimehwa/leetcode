// @lc code=start
class Solution {
    public int maxArea(int[] height) {
        // 这个题好像是用单调栈来做吗？ 或者是滑动窗口？关键是来证明
        
        int max = 0, left = 0, right = height.length - 1;
        while (left < right) {
            max = Math.max(max, Math.min(height[left], height[right]) * (right - left));
            if (height[left] < height[right]) {
                left++;
            } else {
                right--;
            }
        }

        return max;

    }
}
